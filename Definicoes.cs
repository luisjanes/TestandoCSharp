/*Funções ou métodos
 Definições:
- Podemos segmentar nosso programa em funções
- Também conhecidos como métodos
- O main é um exemplo de método
- Possui um retorno, um nome e parâmetros
- Esta composição é a camada assinatura
- Começam sempre com maiúscula


    Heap e Stack:

- A memória é dividida em duas partes, Heap e Stack
- Heap armazena dados
- Stack armazena as referências para os dados
- Values são armazenados no Stack, onde é criada uma "copia"
- Já os reference types, a referencia(endereço de memória) dele na memória é armazenada no Heap.

Nesse caso temos que se você atribuir uma variável como int, string a outra, ela vai copiar o valor. Já se você aatribuir uma CLasse, array, ela vai atribuir a referÇencia na memória.


    Enumeradores:
- Usados pr fornecer uma melhor visuaalizção do código
- Substituem o uso de inteiros
- Usados em listas curtas
- Usado em dados fixos (Hard Coded)
- Sempre em maiúsculo
- Começar com a letra E

Ex: 
    enum ENomedoEnum
    {
        //Enumeradores
        //Enumeradores
    }
 */
